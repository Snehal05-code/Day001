//Define a public class While. the class demonstrates the use of a while loop
public class While {
    //The main method for program execution.
    public static void main(String[] args) {
        //Initialize the integer variable 'cout' with yhe value 1
        int count = 1;
        
        while (count <= 5) {
            //Print the String 'hello'
            System.out.println("hello");
           //Increment  the 'count' by 1
            count++;
        }
    }
}
